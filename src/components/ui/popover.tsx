export const PopoverTrigger = PopoverPrimitive.Trigger;
export const PopoverAnchor = PopoverPrimitive.Anchor;
export const PopoverClose = PopoverPrimitive.Close;

export const PopoverContent = React.forwardRef<
  React.ElementRef<typeof PopoverPrimitive.Content>,
  React.ComponentPropsWithoutRef<typeof PopoverPrimitive.Content>
>(function PopoverContent({ className, align = "center", sideOffset = 6, ...props }, ref) {
  return (
    <PopoverPrimitive.Portal>
      <PopoverPrimitive.Content
        ref={ref}
        align={align}
        sideOffset={sideOffset}
        className={cn(
          "z-overlay min-w-menu rounded-lg border border-border-default bg-surface-overlay p-2 text-text-primary shadow-3 outline-none",
          "data-[state=open]:animate-atlas-fade-in",
          className,
        )}
        {...props}
      />
    </PopoverPrimitive.Portal>
  );
});
